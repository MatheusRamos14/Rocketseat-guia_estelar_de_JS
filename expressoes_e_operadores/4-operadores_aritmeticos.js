// Operadores aritméticos

// Multiplicação
console.log(5 * 5)

// Divisão
console.log(5 / 5)

// Soma
console.log(5 + 5)

// Subtração
console.log(5 - 5)

// Resto de divisão
console.log(5 % 5)

// Incremento
let increment = 4
console.log(++increment)

// Decremento
let decrement = 6
console.log(--decrement)

// Exponencial
console.log(5**5)
